export type SectorId = BaseSectorId | (string & {});

export type Difficulty = "Basica" | "Intermediaria" | "Avancada";
export type UsageLevel = "Baixo" | "Moderado" | "Alto" | "Critico";
export type FormulaStatus = "rascunho" | "em_revisao" | "validada" | "aprovada" | "arquivada";

export interface Sector {
  id: SectorId;
  name: string;
  description: string;
  formulas: number;
  activeCalculations: number;
  health: number;
  trend: string;
  usageLevel: UsageLevel;
  color: string;
  iconName: string;
}

export interface FormulaVariable {
  name: string;
  label: string;
  unit: string;
  placeholder?: string;
}

export interface Formula {
  id: string;
  name: string;
  sectorId: SectorId;
  sector: string;
  expression: string;
  resultUnit: string;
  description: string;
  variables: FormulaVariable[];
  difficulty: Difficulty;
  usageCount: number;
  example: string;
  simpleExplanation: string;
  tags: string[];
  status: FormulaStatus;
  createdBy: string;
  approvedBy?: string;
  createdAt: string;
  approvedAt?: string;
  version: number;
  technicalNotes?: string;
  isCustom?: boolean;
}

export interface CalculationRecord {
  id: number;
  formulaId: string;
  formula: string;
  sector: string;
  sectorId: SectorId;
  values: Record<string, string>;
  result: string;
  unit: string;
  formulaStatus: FormulaStatus;
  status: "Validado" | "Rascunho" | "Revisao";
  operator: string;
  date: string;
  isoDate: string;
}

type BaseSector = Omit<Sector, "formulas">;

type FormulaSeed = Omit<
  Formula,
  "sector" | "difficulty" | "usageCount" | "status" | "createdBy" | "approvedBy" | "createdAt" | "approvedAt" | "version"
> & {
  difficulty?: Difficulty;
  usageCount?: number;
  status?: FormulaStatus;
  createdBy?: string;
  approvedBy?: string;
  createdAt?: string;
  approvedAt?: string;
  version?: number;
};

